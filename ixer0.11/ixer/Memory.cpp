//--------------------------------内存操作------------------------------------------

////////////////////////////////////////////////////////////////////////////////////
//
//	功能实现：申请堆内存
//	输入参数：Length为申请的缓冲区长度
//	输出参数：失败返回NULL，成功则返回申请的内存缓冲区指针
//
////////////////////////////////////////////////////////////////////////////////////
PVOID
AllocHeapMem(DWORD	Length)
{
	TRACE0("申请内存");
	if(Length <= 0)
	{
		return NULL;
	}
	return GlobalAlloc(GPTR, Length);
}

///////////////////////////////////////////////////////////////////////////////////
//
//	功能实现：释放堆内存
//	输入参数：pMem为要释放的缓冲区指针
//	输出参数：无
//
///////////////////////////////////////////////////////////////////////////////////
void
FreeHeapMem(PVOID	pMem)
{
	TRACE0("释放内存");
	GlobalFree(pMem);
}